function mostrar() {
    document.getElementById('CardContent').style.display = 'block';
    document.getElementById('btnHidden').style.display = 'block';
    document.getElementById('btnShow').style.display = 'none';
    
}
function ocultar() {
    document.getElementById('CardContent').style.display = 'none';
    document.getElementById('btnHidden').style.display = 'none';
    document.getElementById('btnShow').style.display = 'block';
    
}